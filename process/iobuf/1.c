// Приклад використання виходу з програми exit()
// ідентичний поверненню із головної функції

#include <unistd.h>
#include <stdlib.h>
#include <stdio.h>

void function(void)
{
  // Це має бути видрукувано, оскільки exit() просуває буфер виводу
  printf("Hello word");
  // Стандартний вихід із процесу
  exit(0);
}

int main(){
  // Викликаємо просту функцію, яка завершить процес
  function();
  printf("Це не має бути видрукувано\n");
  return 0;
}
